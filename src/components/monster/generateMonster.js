/**
 * @param {string} dateString 形如 '2026-06-04'
 * @returns {{ width: number, height: number, grid: number[][], color: string }}
 *   grid: 8 行 × 9 列，每格 0(空) / 1(身体) / 2(眼睛)；color: hsl 主色
 */
export function generateMonster(dateString) {
  const rand = mulberry32(hashSeed(dateString));
  const shape = SHAPES[Math.floor(rand() * SHAPES.length)];
  const grid = shape.map(row => row.slice()); // 拷贝，避免调用方污染造型库
  const hue = Math.floor(rand() * 360);
  const color = `hsl(${hue}, 65%, 55%)`;
  return { width: grid[0].length, height: grid.length, grid, color };
}
