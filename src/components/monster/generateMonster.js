/**
 * 每日像素怪兽生成器（纯函数，零 API）
 *
 * 审美靠「设计」不靠「随机」：维护一组手工设计的极简纯方块造型，
 * 每天用日期种子随机挑一个 + 随机配色。
 *  - 纯方块、单色、左右对称；脸上嵌一对方块眼睛（值 2）点明"是个怪兽"
 *  - 同一天 → 同一只（种子稳定）
 *  - 不同天 → 大概率不同（挑不同造型 / 不同颜色）
 */

// 字符串 → 32 位无符号种子（FNV-1a）
function hashSeed(str) {
  let h = 2166136261 >>> 0;
  for (let i = 0; i < str.length; i++) {
    h ^= str.charCodeAt(i);
    h = Math.imul(h, 16777619) >>> 0;
  }
  return h >>> 0;
}

// mulberry32：确定性 PRNG，返回 [0,1)
function mulberry32(seed) {
  let a = seed >>> 0;
  return function () {
    a = (a + 0x6D2B79F5) | 0;
    let t = Math.imul(a ^ (a >>> 15), 1 | a);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

